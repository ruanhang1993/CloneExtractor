package cn.edu.fudan.se.clonedetector.bean;

public class CCStreamProperty {
	private String stream;
	private String blstream;
	private String view;
	private String viewLocalPath;
	public String getStream() {
		return stream;
	}
	public void setStream(String stream) {
		this.stream = stream;
	}
	public String getBlstream() {
		return blstream;
	}
	public void setBlstream(String blstream) {
		this.blstream = blstream;
	}
	public String getView() {
		return view;
	}
	public void setView(String view) {
		this.view = view;
	}
	public String getViewLocalPath() {
		return viewLocalPath;
	}
	public void setViewLocalPath(String viewLocalPath) {
		this.viewLocalPath = viewLocalPath;
	}
	
}
